package com.jfehr.tojs.testutil;

public class TestUtilException extends RuntimeException {

	private static final long serialVersionUID = -4509835111523765993L;

	public TestUtilException() {
	}

	public TestUtilException(String arg0) {
		super(arg0);
	}

	public TestUtilException(Throwable arg0) {
		super(arg0);
	}

	public TestUtilException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

}
